    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        // TODO add your handling code here:
        String id = bId.getText();
        
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter book id.");
        }
        else{
            int confirmDelete = JOptionPane.showConfirmDialog(this, "Do you want to delete the book?", "warning", JOptionPane.YES_NO_OPTION);
            if(confirmDelete == JOptionPane.YES_OPTION){
                try{
                    String sql = "delete from books where Book_id=?";
                    ps = ConnectionClass.getConnection().prepareStatement(sql);
                    ps.setString(1, id);
                    int rs = ps.executeUpdate();
                    if(rs == 1){
                        JOptionPane.showMessageDialog(this, "Book deleted successfully.");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Book not found.");
                    }
                    setFieldsNull();
                }
                catch(SQLException e){
                    System.out.println("Error" + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_deleteBtnMouseClicked

    private void searchBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchBtnMouseClicked
        // TODO add your handling code here:
        String b_Id = bId.getText();
        
        if(b_Id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter book id.");
            setFieldsNull();
        }
        else{
            try{
                String sql0 = "select Book_id from books where Book_id=?";
                ps = ConnectionClass.getConnection().prepareStatement(sql0);
                ps.setString(1, b_Id);
                ResultSet r = ps.executeQuery();
                int b_id = 0;
                if(r.absolute(1)){
                    try{
                            String sql = "select*from books where Book_id=?";
                            ps = ConnectionClass.getConnection().prepareStatement(sql);
                            ps.setString(1, b_Id);
                            ResultSet rs = ps.executeQuery();

                            while(rs.next()){
                                String id = rs.getString("Book_id");
                                String title = rs.getString("Title");
                                String author = rs.getString("Author");
                                String isbn = rs.getString("Isbn");
                                String publisher = rs.getString("Publisher");
                                String price = rs.getString("Price");
            //                    System.out.println(id + " "+ title+" "+author+" "+isbn+" "+publisher+" "+price);

                                bTitle.setText(title);
                                bAuthor.setText(author);
                                bIsbn.setText(isbn);
                                bPublisher.setText(publisher);
                                bPrice.setText(price);
                            }               
                    }
                    catch(SQLException e){
                        System.out.println("Error" + e.getMessage());
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Book not found.");
                    setFieldsNull();
                }
            }
            catch(SQLException ex){
                System.out.println(ex);
            }
            
        }
    }//GEN-LAST:event_searchBtnMouseClicked

    private void updateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseClicked
        // TODO add your handling code here:
        String id = bId.getText();
        String title = bTitle.getText();
        String author = bAuthor.getText();
        String publisher = bPublisher.getText();
        String price = bPrice.getText();
        
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter book id.");
            setFieldsNull();
        }
        else{
            try{
                String sql = "select Book_id from books where Book_id='"+id+"'";
//                System.out.println(sql);
                ps = ConnectionClass.getConnection().prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                int id1 = 0;
                if(rs.absolute(1)){
                    id1 = rs.getInt("Book_id");
//                    System.out.println(id1);
                    try{
                        String sql1 = "update books set Title=?, Author=?, Publisher=?, Price=? where Book_id=?";
                        ps = ConnectionClass.getConnection().prepareStatement(sql1);
                        ps.setString(1, title);
                        ps.setString(2, author);
                        ps.setString(3, publisher);
                        ps.setString(4, price);
                        ps.setString(5, id);
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Information updated successfully.");
                        setFieldsNull();
                    }
                    catch(SQLException ee){
                        System.out.println(ee.getMessage());
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Book not found.");
                    setFieldsNull();
                }
            }
            catch(SQLException ee){
                System.out.println(ee);
            }
        }
        
    }//GEN-LAST:event_updateBtnMouseClicked

