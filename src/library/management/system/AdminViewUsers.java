    private void getData(){
        ResultSet rs;
        try{
            String sql = "select Student_id, Name, Course, Year, Semester from students";
            ps = ConnectionClass.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();
            DefaultTableModel tblModel = (DefaultTableModel) studentsTable.getModel();
            
            while(rs.next()){
                String id = String.valueOf(rs.getString("Student_id"));
                String name = rs.getString("Name");
                String course = rs.getString("Course");
                String year = rs.getString("Year");
                String sem = rs.getString("Semester");
                
                String tblData[] = {id, name, course, year, sem};
                tblModel.addRow(tblData);
            }
        }
        catch(SQLException e){
            System.out.println(e);
        }
    }
    
