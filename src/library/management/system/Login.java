    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
          int acc = accountType.getSelectedIndex();
//          System.out.println(acc);

          String adminUsername = "admin";
          String adminPassword = "admin";
          
          String name = uName.getText();
//          System.out.println(name);
          String password = uPassword.getText(); 
//          System.out.println(password);  
          
          if(acc == 0){
//              System.out.println("Admin Account");
              if(adminUsername.equals(name) && adminPassword.equals(password)){
                  this.dispose();
                  new AdminHome().setVisible(true);
              }
              else{
                  JOptionPane.showMessageDialog(null, "Username or password is incorrect.");
                  uName.setText("");
                  uPassword.setText("");
              }
          }
          else{
              String dbUsername = "";
              String dbPassword = "";
              
              if(acc == 1){
//                  System.out.println("User Account");
                 try{
                      /*** Check user account username and password ***/
                      String sql = "select * from students where Username='"+name+"' && Password='"+password+"'";
                      ps = ConnectionClass.getConnection().prepareStatement(sql);
                      ResultSet rs = ps.executeQuery();
                      
                      while(rs.next()){
                         /*** username and password from database ***/
                          dbUsername = rs.getString("Username");
                          dbPassword = rs.getString("Password");
                      }
                /*** checking username,password entered is equal to username,password from database  ***/
                      if(name.equals(dbUsername) && password.equals(dbPassword)){
                          this.dispose();
                          new UserHome().setVisible(true);
                          System.out.println("User login successful.");
                      }
                      else{
                          JOptionPane.showMessageDialog(null, "Username or password is incorrect.");
                          System.out.println("User login failed.");
                          uName.setText("");
                          uPassword.setText("");
                      }
                 }
                 catch(SQLException e){
                     System.out.println(e);
                 }
              }
          }
    }//GEN-LAST:event_loginBtnActionPerformed

