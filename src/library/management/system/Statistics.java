    private void getIssueDetails(){
        ResultSet rs, ri, rr;
        try{
            String sql0 = "select*from issue_books";
            ps = ConnectionClass.getConnection().prepareStatement(sql0);
            rs = ps.executeQuery();
            String bookId, title, studentId, name, issueDate, returnDate;
            DefaultTableModel tblModel1 = (DefaultTableModel) issueTable.getModel();
            
            while(rs.next()){
                
                bookId = String.valueOf(rs.getInt("Book_id"));
                studentId = String.valueOf(rs.getInt("Student_id"));
                issueDate = rs.getString("Issue_date");
                returnDate = rs.getString("Return_date");
                
                String sql1 = "select Name from students where Student_id=?";
                ps = ConnectionClass.getConnection().prepareStatement(sql1);
                ps.setString(1, studentId);
                ri = ps.executeQuery();
                
                String sql2 = "select Title from books where Book_id=?";
                ps = ConnectionClass.getConnection().prepareStatement(sql2);
                ps.setString(1, bookId);
                rr = ps.executeQuery();
                
                while(ri.next() && rr.next()){
                    name = ri.getString("Name");
                    title = rr.getString("Title");
                    String tblData[] = {studentId, name, bookId, title, issueDate, returnDate };
                    tblModel1.addRow(tblData);
                }                 
            }
        }
        catch(SQLException e){
            System.out.println(e);
        }
    }
    
