    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        // TODO add your handling code here:
        String id = sId.getText();
        
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter student id.");
            setFieldsNull();
        }
        else{
            int confirmDelete = JOptionPane.showConfirmDialog(this, "Do you want to delete the student?", "warning", JOptionPane.YES_NO_OPTION);
            if(confirmDelete == JOptionPane.YES_OPTION){
                try{
                    String sql = "delete from students where Student_id=?";
                    ps = ConnectionClass.getConnection().prepareStatement(sql);
                    ps.setString(1, id);
                    int rs = ps.executeUpdate();
                    if(rs == 1){
                        JOptionPane.showMessageDialog(this, "Student deleted successfully.");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Student not found.");
                        setFieldsNull();
                    }
                    setFieldsNull();
                }
                catch(SQLException e){
                    System.out.println("Error" + e.getMessage());
                }
            }
        }
        
        
    }//GEN-LAST:event_deleteBtnMouseClicked

    private void searchBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchBtnMouseClicked
        // TODO add your handling code here:
        String s_Id = sId.getText();
        
        if(s_Id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter student id.");
            setFieldsNull();
        }
        else{
            try{
                String sql0 = "select Student_id from students where Student_id=?";
                ps = ConnectionClass.getConnection().prepareStatement(sql0);
                ps.setString(1, s_Id);
                ResultSet r = ps.executeQuery();
                if(r.absolute(1)){
                    try{
                        String sql = "select*from students where Student_id=?";
                        ps = ConnectionClass.getConnection().prepareStatement(sql);
                        ps.setString(1, s_Id);
                        ResultSet rs = ps.executeQuery();
                
                        while(rs.next()){
                            String id = rs.getString("Student_id");
                            String name = rs.getString("Name");
                            String course = rs.getString("Course");
                            String year = rs.getString("Year");
                            String sem = rs.getString("Semester");
                            String username = rs.getString("Username");
                            String password = rs.getString("Password");
        //                    System.out.println(id + " "+ name+" "+course+" "+year+" "+sem+" "+username+" "+password);

                            sName.setText(name);
                            sCourse.setText(course);
                            sYear.setText(year);
                            sSem.setText(sem);
                            sUName.setText(username);
                            sUPassword.setText(password);
                        }               
                    }
                    catch(SQLException e){
                        System.out.println("Error" + e.getMessage());
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Student not found.");
                    setFieldsNull();
                }
            }
            catch(SQLException ee){
                System.out.println(ee);
            }
            
        }
    }//GEN-LAST:event_searchBtnMouseClicked

    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked
        // TODO add your handling code here:
        String id = sId.getText();
        String name = sName.getText();
        String course = sCourse.getText();
        String year = sYear.getText();
        String sem = sSem.getText();
        String username = sUName.getText();
        String password = sUPassword.getText();
        
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter student id.");
            setFieldsNull();
        }
        else{
            try{
                String sql0 = "select Student_id from students where Student_id=?";
                ps = ConnectionClass.getConnection().prepareStatement(sql0);
                ps.setString(1, id);
                ResultSet r = ps.executeQuery();
                if(r.absolute(1)){
                    try{
                        String sql = "update students set Name=?, Course=?, Year=?, Semester=?, Username=?, Password=? where Student_id=?";
                        ps = ConnectionClass.getConnection().prepareStatement(sql);
                        /*** Order of query ***/
                        ps.setString(1, name);
                        ps.setString(2, course);
                        ps.setString(3, year);
                        ps.setString(4, sem);
                        ps.setString(5, username);
                        ps.setString(6, password);
                        ps.setString(7, id);
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Information updated successfully.");
                        setFieldsNull();
                    }
                    catch(SQLException ee){
                        System.out.println(ee.getMessage());
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Student not found.");
                    setFieldsNull();
                }
            }
            catch(SQLException ee){
                System.out.println(ee);
            }
            
        }
    }//GEN-LAST:event_UpdateBtnMouseClicked

