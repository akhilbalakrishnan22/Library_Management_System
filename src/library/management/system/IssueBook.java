    private void issueBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_issueBtnMouseClicked
        // TODO add your handling code here:
        String b_Id = bId.getText();
        String s_Id = sId.getText();
        String issue_date = iDate.getSelectedItem().toString() +"/"+ iMonth.getSelectedItem().toString()+"/"+ iYear.getSelectedItem().toString();
        String return_date = rDate.getSelectedItem().toString()+"/"+ rMonth.getSelectedItem().toString()+"/"+ rYear.getSelectedItem().toString();
        
        if(b_Id.isEmpty() || s_Id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter book id and student id");
            setFieldsNull();
        }
        else{
            try{
                String sql0 = "select Book_id from books where Book_id=?";
                ps = ConnectionClass.getConnection().prepareStatement(sql0);
                ps.setString(1, b_Id);
//                System.out.println(sql0);
                ResultSet rs = ps.executeQuery();
                
                String sql1 = "select Student_id from students where Student_id=?";
                ps = ConnectionClass.getConnection().prepareStatement(sql1);
                ps.setString(1, s_Id);
//                System.out.println(sql1);
                ResultSet r = ps.executeQuery();
                
                int book_Id = 0;
                int stu_Id = 0;
                
                if(rs.absolute(1) && r.absolute(1)){
                    book_Id = rs.getInt("Book_id");
//                    System.out.println(book_id);
                    stu_Id = r.getInt("Student_id");
//                    System.out.println(stu_Id);

                    try{
                        String sql2 = "select Book_id, Student_id from issue_books where Book_id=? and Student_id=?";
                        ps = ConnectionClass.getConnection().prepareStatement(sql2);
                        ps.setString(1, b_Id);
                        ps.setString(2, s_Id);
                        ResultSet rx = ps.executeQuery();
                        
                        if(rx.absolute(1)){
                            JOptionPane.showMessageDialog(this, "Book already issued to that student.");
                            setFieldsNull();
                        }
                        else{
                            try{
                                String sql = "insert into issue_books(Book_id, Student_id, Issue_date, Return_date) values('"+book_Id+"','"+stu_Id+"','"+issue_date+"','"+return_date+"')";
                                ps = ConnectionClass.getConnection().prepareStatement(sql);
                                ps.executeUpdate();
                                JOptionPane.showMessageDialog(this, "Booke issued successfully.");
                                setFieldsNull();
                            }
                            catch(SQLException ee){
                                System.out.println(ee);
                            }
                        }
                    }
                    catch(SQLException ex){
                        System.out.println(ex);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Student or book not found.");
                    setFieldsNull();
                }
            }
            catch(SQLException e){
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_issueBtnMouseClicked
    
