    private void getData(){
        ResultSet rs;
        try{
            String sql = "select*from books";
            ps = ConnectionClass.getConnection().prepareStatement(sql);
            rs = ps.executeQuery(); 
            DefaultTableModel tblModel = (DefaultTableModel) booksTable.getModel();
            
             while(rs.next()){
                    String id = String.valueOf(rs.getInt("Book_id"));
                    String title = rs.getString("Title");
                    String author = rs.getString("Author");
                    String isbn = rs.getString("Isbn");
                    String publisher = rs.getString("Publisher");
                    String price = rs.getString("Price");
    //                System.out.println(id+" "+title+" "+author+" "+isbn+" "+publisher+" "+price);
                    String tblData[] = {id, title, author, isbn, publisher, price};
                    tblModel.addRow(tblData);
                }
            
        }
        catch(SQLException e){
            System.out.println("Error "+ e.getMessage());
            
        }
        
    }

