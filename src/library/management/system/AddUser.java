    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        // TODO add your handling code here:
        String id = sId.getText();
        String name = sName.getText();
        String course = sCourse.getText();
        String year = sYear.getText();
        String sem = sSem.getText();
        String username = sUName.getText();
        String password = sUPassword.getText();
//        System.out.println(id+ " " +name+ " " +course+" " +year+" "+ sem +" "+ username+" "+ password);
        String confirmPassword = sCPassword.getText();
        
        if(id.isEmpty()|| name.isEmpty() || course.isEmpty() || year.isEmpty() || sem.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter student details.");
        }
        else if(username.isEmpty() && password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Username and password is compulsory");
        }
        else{
            try{
                if(confirmPassword.equals(password)){
                    String sql = "insert into students(Student_id, Name, Course, Year, Semester, Username, Password) values('"+id+"','"+name+"','"+course+"','"+year+"','"+sem+"','"+username+"','"+password+"')";
                    ps = ConnectionClass.getConnection().prepareStatement(sql);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(this, "New student added successfully."); 
                    setFieldsNull();                
                }
                else{
                    JOptionPane.showMessageDialog(this, "Passwords not matching");
                    sUPassword.setText("");
                    sCPassword.setText("");
                }
            }
            catch(SQLException ee){
                System.out.println(ee);
            }
        } 
    }//GEN-LAST:event_addBtnMouseClicked

